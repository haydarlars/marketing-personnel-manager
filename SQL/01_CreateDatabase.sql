-- DROP existing tables (in correct order due to FK constraints)
-- ============================================================
IF OBJECT_ID('dbo.Sales', 'U') IS NOT NULL DROP TABLE dbo.Sales;
IF OBJECT_ID('dbo.Personnel', 'U') IS NOT NULL DROP TABLE dbo.Personnel;
GO

-- ============================================================
-- TABLE: Personnel
-- ============================================================
CREATE TABLE Personnel (
    Id      INT             IDENTITY(1,1) PRIMARY KEY,
    Name    VARCHAR(50)     NOT NULL,
    Age     INT             NOT NULL,
    Phone   VARCHAR(20)     NOT NULL,
    CONSTRAINT CK_Personnel_Age CHECK (Age > 18)
);
GO

-- ============================================================
-- TABLE: Sales
-- ============================================================
CREATE TABLE Sales (
    Id              INT             IDENTITY(1,1) PRIMARY KEY,
    PersonnelId     INT             NOT NULL,
    ReportDate      DATETIME        NOT NULL,
    SalesAmount     DECIMAL(10,2)   NOT NULL,
    CONSTRAINT FK_Sales_Personnel FOREIGN KEY (PersonnelId)
        REFERENCES Personnel(Id) ON DELETE CASCADE
);
GO

-- ============================================================
-- INDEX: Speed up sales lookups by personnel
-- ============================================================
CREATE INDEX IX_Sales_PersonnelId ON Sales(PersonnelId);
GO

